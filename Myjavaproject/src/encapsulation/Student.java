package encapsulation;

public class Student {

	private String name;
	private int age;
	
	public void setName(String a) {
		this.name=a;
		
	}
	public String getName() {
		return name;
		
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	}


